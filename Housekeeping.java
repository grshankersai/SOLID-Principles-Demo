class Housekeeping{
    private String name;
    private int employeeId;
    private int tip;
    public Housekeeping(String name , int id, int tip){
        this.name = name;
        this.employeeId = id;
        this.tip = tip;
    }

    public String getName() {
        return name;
    }

    public int getEmployeeId() {
        return employeeId;
    }

    public int getTip() {
        return tip;
    }
    // few methods.

}