class Housekeeping{
    private String name;
    private int emp_id;
    private int tip;
    public Housekeeping(String name , int id, int tip){
        this.name = name;
        this.emp_id = id;
        this.tip = tip;
    }

    public String getName() {
        return name;
    }

    public int getEmp_id() {
        return emp_id;
    }

    public int getTip() {
        return tip;
    }
    // few methods.

}